#include "libft.h"

int		ft_atoi(const char *str)
{
	int				i;
	long long int	sign;
	long long int	result;

	i = 0;
	result = 0;
	sign = 1;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (result * 10) + (str[i] - '0');
		i++;
	}
	if ((ft_checkf(result, sign)) == -1)
		return (-1);
	if ((ft_checkf(result, sign)) == 0)
		return (0);
	return (result * sign);
}
